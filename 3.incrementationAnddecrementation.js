// L'INCREMENTATION ET LA DECREMENTATION

/* A. L'INCREMENTATION */

let calcul = 10;
calcul = calcul + 1;
// console.log(calcul)

calcul += 1;
// console.log(calcul)

calcul ++;
// console.log(calcul)



/* B. LA DECREMENTATION */

calcul = calcul - 1;
// console.log(calcul)

calcul -= 1;
// console.log(calcul)

calcul --;
// console.log(calcul)